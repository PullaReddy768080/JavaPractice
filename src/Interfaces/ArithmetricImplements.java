package Interfaces;

public class ArithmetricImplements implements ArithmetricInterface
{
    public int addition(int a, int b) {
        return a+b;

    }
    public int subtraction(int a, int b) {
        return a-b;

    }
    public int multiplication(int a, int b) {
        return a*b;

    }
    public int divison(int a, int b) {
        return a/b;

    }


}


